public class IntegerLiteral{
    public static void main(String[] args){
        byte b1=10; //decimal
        byte b2=0b1010; //binary
        byte b3=012; //octal
        byte b4=0XA; //hexadecimal

        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
        System.out.println(b4);
    }
}